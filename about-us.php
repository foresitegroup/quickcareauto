Quick Care Auto, formerly Richardson's Personal Auto, owned and operated by Russ Richardson is now Quick Care Auto and operates with the same personal style. While many upgrades to the building have changed, existing customers will recognize the excellent services at local prices.  Our employee Anthony Reyes will keep your car running in tip top shape!  See below to learn more about about Anthony.<br>
